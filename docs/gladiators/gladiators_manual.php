<h2><a name="faqs"></a><font color="#008000">FAQs</font></h2>
<ul>
  <li><i><b>What Do I Do When I Find A Bug?</b></i>
    <p>Bugs can be reported by creating a bug report on Sourceforge. <a
 href="/bug.html">To
find out in detail what todo click here and visit the appropriate page
on our website</a>.</p>
    <p>&nbsp; </p>
  </li>
  <li><b><i>What Does The Little Flashing Plug Graphic Mean?</i></b>
    <p>This image means you have been disconnected from the server.
Close Gladiators by pressing Escape, reload it and login again to
continue.</p>
    <p>&nbsp; </p>
  </li>
  <li><i><b>How Can I Turn Off The Fullscreen Mode?</b></i>
    <p>Add the -w command line parameter. For example, Do this:</p>
    <ul>
      <li>Right Click On the Short cut to Gladiators in the Start Menu.</li>
      <li>Click Properties</li>
      <li>In the window that appears, add -w after the text in the
Target box. (to something like "c:\program
files\gladiators\gladiators.exe" -w)</li>
      <li>Click OK</li>
      <li>Run gladiators from this short cut.</li>
    </ul>
    <p>&nbsp;
    </p>
  </li>
</ul>

