<h2><a name="about"></a><font style="font-weight: bold;"
 color="#008000">About</font></h2>
<p>Gladiators is a game designed to prove and test the ideas and
functions of the Arianne System. <br>
Thus, though it is a fully playable game, it is simple and to the point!<br>
The main objective is to command gladiators, that you own, in the arena
and gain fame and hence fortune! <br>
This document was prepared to explain the usage and reason behind the
game. </p>
